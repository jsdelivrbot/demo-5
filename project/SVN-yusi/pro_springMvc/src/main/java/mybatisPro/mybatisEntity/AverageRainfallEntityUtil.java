package mybatisPro.mybatisEntity;

import java.util.List;
import java.util.Map;

import mybatisPro.mybatisEntity.StationInfoEntityUtil;

/**
 * 区域平均降雨量计算工具类
 * @author si.yu
 * @date 2017/07/25
 * @version 1.0
 * */
public class AverageRainfallEntityUtil {

	private Long area_id;//区域ID
	private String area_name;//区域名称
	private Integer algorithmType;//区域降雨量算法
	private List<StationInfoEntityUtil> station_info_list;
	private Float average_rainfall;//区域平均降雨量
	private List<Map<String, Object>> param_list;
	private String maxStation;//该区域最大降雨量站名
	private Float max_Rainfall;//最大降雨量
	private Long maxStation_id;//最大雨量站ID
	private String message;//该区域未部署雨量站
	
	
	
	public Long getArea_id() {
		return area_id;
	}
	public void setArea_id(Long area_id) {
		this.area_id = area_id;
	}
	
	public String getArea_name() {
		return area_name;
	}
	public void setArea_name(String area_name) {
		this.area_name = area_name;
	}
	
	public Integer getAlgorithmType() {
		return algorithmType;
	}
	public void setAlgorithmType(Integer algorithmType) {
		this.algorithmType = algorithmType;
	}
	
	public List<StationInfoEntityUtil> getStation_info_list() {
		return station_info_list;
	}
	public void setStation_info_list(List<StationInfoEntityUtil> station_info_list) {
		this.station_info_list = station_info_list;
	}
	
	public Float getAverage_rainfall() {
		return average_rainfall;
	}
	public void setAverage_rainfall(Float average_rainfall) {
		this.average_rainfall = average_rainfall;
	}
	
	public List<Map<String, Object>> getParam_list() {
		return param_list;
	}
	public void setParam_list(List<Map<String, Object>> param_list) {
		this.param_list = param_list;
	}
	
	public String getMaxStation() {
		return maxStation;
	}
	public void setMaxStation(String maxStation) {
		this.maxStation = maxStation;
	}
	
	public Float getMax_Rainfall() {
		return max_Rainfall;
	}
	public void setMax_Rainfall(Float max_Rainfall) {
		this.max_Rainfall = max_Rainfall;
	}
	
	public Long getMaxStation_id() {
		return maxStation_id;
	}
	public void setMaxStation_id(Long maxStation_id) {
		this.maxStation_id = maxStation_id;
	}
	
	public String getMessage() {
		return message;
	}
	public void setMessage(String message) {
		this.message = message;
	}
	
}
