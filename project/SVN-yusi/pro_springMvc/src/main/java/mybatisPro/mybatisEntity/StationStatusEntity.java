package mybatisPro.mybatisEntity;

import java.io.Serializable;

/**
 * 测站状态实体类
 * 
 * @author feng.gao
 * @date 2017年8月18日
 * @version 1.0
 */
public class StationStatusEntity implements Serializable {

	private static final long serialVersionUID = 1L;

	private Long id;
	private Long sta_id;
	private Long type;
	private String date_time;
	private String create_time;

	@Override
	public String toString() {
		return "StationStatusEntity [id=" + id + ", sta_id=" + sta_id + ", type=" + type + ", date_time=" + date_time
				+ ", create_time=" + create_time + "]";
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Long getSta_id() {
		return sta_id;
	}

	public void setSta_id(Long sta_id) {
		this.sta_id = sta_id;
	}

	public Long getType() {
		return type;
	}

	public void setType(Long type) {
		this.type = type;
	}

	public String getDate_time() {
		return date_time;
	}

	public void setDate_time(String date_time) {
		this.date_time = date_time;
	}

	public String getCreate_time() {
		return create_time;
	}

	public void setCreate_time(String create_time) {
		this.create_time = create_time;
	}

}
