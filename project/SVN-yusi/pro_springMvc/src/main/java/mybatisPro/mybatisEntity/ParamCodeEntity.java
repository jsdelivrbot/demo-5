package mybatisPro.mybatisEntity;

import java.io.Serializable;

/**
 * 参数编码与测站关系实体类
 * @author si.yu
 * @date 2017/08/25
 * @version 1.0  
 */
public class ParamCodeEntity implements Serializable{

	private static final long serialVersionUID = 1L;
	
	private Long sta_id;//测站ID
	private String param_id;//参数编码
	private Float corvalue;//修正值
	
	@Override
	public String toString(){
		return "ParamCodeEntity[sta_id="+sta_id+",param_id="+param_id
				+",corvalue="+corvalue+"]";
	}
	
	public Long getSta_id() {
		return sta_id;
	}
	public void setSta_id(Long sta_id) {
		this.sta_id = sta_id;
	}
	public String getParam_id() {
		return param_id;
	}
	public void setParam_id(String param_id) {
		this.param_id = param_id;
	}
	public Float getCorvalue() {
		return corvalue;
	}
	public void setCorvalue(Float corvalue) {
		this.corvalue = corvalue;
	}
	
	
}
