package mybatisPro.mybatisEntity;

import java.io.Serializable;
import java.util.Date;

/**
 * 用户信息实体类
 * @author si.yu
 * @date 2017/6/14
 * */
public class UserEntity implements Serializable{
	
	private static final long serialVersionUID = 1L;
	
	private Integer id;//用户ID
	private String username;//用户名
	private String role_id;//角色IDs
	private String password;//密码
	private String salt;//盐
	private String telephone;//联系人电话
	private Date create_time = new Date();
	private Date modify_time = new Date();
	
	
	@Override
	public String toString(){
		return "UserEntity[id="+id+",username="+username+",role_id="+role_id+",password="+password
				+",salt="+salt+",telephone="+telephone+",create_time="+create_time+",modify_time="+modify_time+"]";
	}


	public Integer getId() {
		return id;
	}


	public void setId(Integer id) {
		this.id = id;
	}


	public String getUsername() {
		return username;
	}


	public void setUsername(String username) {
		this.username = username;
	}


	public String getRole_id() {
		return role_id;
	}


	public void setRole_id(String role_id) {
		this.role_id = role_id;
	}


	public String getPassword() {
		return password;
	}


	public void setPassword(String password) {
		this.password = password;
	}


	public String getSalt() {
		return salt;
	}


	public void setSalt(String salt) {
		this.salt = salt;
	}


	public String getTelephone() {
		return telephone;
	}


	public void setTelephone(String telephone) {
		this.telephone = telephone;
	}


	public Date getCreate_time() {
		return create_time;
	}


	public void setCreate_time(Date create_time) {
		this.create_time = create_time;
	}


	public Date getModify_time() {
		return modify_time;
	}


	public void setModify_time(Date modify_time) {
		this.modify_time = modify_time;
	}
	

	
	
}
