package ekuter.mvc.util;

/**
 * ajax请求返回信息内容工具类
 * @author si.yu
 * @date 2017/6/20
 * @version 1.0
 * */
public class ResultMessageUtil {
	
	private Integer status;//返回数据状态
	private String msg;//返回数据信息
	private Object data;//返回数据对象
	private Object dataSub;//返回备用附加数据对象
	
	
	
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public Object getData() {
		return data;
	}
	public void setData(Object data) {
		this.data = data;
	}
	public Object getDataSub() {
		return dataSub;
	}
	public void setDataSub(Object dataSub) {
		this.dataSub = dataSub;
	}
	
	
}
